    {
        $this->validate($request, [
            'title' => 'required'
        ]);
        $data = $request->all();
        try {
            Stock::create($data);
            return redirect()->back()->with('success', 'Stock created successfully');
        } catch (\Exception $e) {
            return redirect()->back()->with('error', $e->getMessage());
        }
    }

